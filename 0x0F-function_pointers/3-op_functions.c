#include "function_pointers.h"

/**
* op_add - function to add two integers together
* @a: the first integer
* @b: the second integer
*
* Return: the sum of the two integers
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - function to subtract one integer from another
* @a: the first integer
* @b: the second integer
*
* Return: the difference of the two integers
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - function to multiply two integers together
* @a: the first integer
* @b: the second integer
*
* Return: the product of the two integers
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - function to divide two integers
* @a: the first integer
* @b: the second integer
*
* Return: the quotient
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - asdf asdf asdf asdf asdf asdf
* @a: the first integer
* @b: the second integer
*
* Return: the sum of the two integers
*/
int op_mod(int a, int b)
{
	return (a % b);
}

